package com.cncoman.product.bean;

public class BakUrlInfo{
	private int id;
	private String bakurl;
	private int pord;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getBakurl() {
		return bakurl;
	}
	public void setBakurl(String bakurl) {
		this.bakurl = bakurl;
	}
	public int getPord() {
		return pord;
	}
	public void setPord(int pord) {
		this.pord = pord;
	}
}
